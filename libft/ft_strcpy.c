#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int d;
	int s;

	d = 0;
	s = 0;
	while (src[s] != '\0')
	{
		dst[d] = src[s];
		d++;
		s++;
	}
	dst[d] = '\0';
	return (dst);
}
