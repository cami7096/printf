#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	char	*s1;

	i = 0;
	s1 = s;
	if (!s || !f)
		return ;
	while (s1[i] && s)
	{
		f(&s1[i]);
		i++;
	}
}
